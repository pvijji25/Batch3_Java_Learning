package day6;

public class BaseMethod {

    //Method to check Vowels & consonant

    public String checkVowel(char c) {
        switch (c) {

            case 'a':
                return "vowel";

            case 'A':
                return "vowel";

            case 'e':
                return "vowel";

            case 'i':
                return "vowel";

            case '0':
                return "vowel";


            case 'u':
                return "vowel";


            default:
                return "consonant";


        }
    }

}














